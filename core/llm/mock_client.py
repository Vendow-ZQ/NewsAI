    def _extract_title(self, prompt: str) -> str:
        """从prompt中提取选题标题。"""
        try:
            for line in prompt.split('\n'):
                if '选题标题' in line:
                    return line.split('：', 1)[-1].strip()[:20]
        except:
            pass
        return "AI新动态"


